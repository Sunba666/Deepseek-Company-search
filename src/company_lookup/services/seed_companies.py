def get_all_seeds() -> list:
    """返回所有种子企业名称（扁平列表，去重）。"""
    seen = set()
    result = []
    for industry, companies in SEED_COMPANIES.items():
        for name in companies:
            if name not in seen:
                seen.add(name)
                result.append((name, industry))
    return result


